"""
Ejercicio 4. Pedir dos números al usuario y hacerr todas las operaciones básicas de una calculadora
y mostrar por pantalla
"""
numero1 = int(input("Introduce el primer número: "))
numero2 = int(input("Introduce el segundo número: "))

# Operaciones de calculadora
print("############# CALCULADORA ##############################")
print(f"La SUMA de los dos número es {numero1 + numero2}")
print(f"La RESTA de los dos número es {numero1 - numero2}")
print(f"La MULTIPLICACIÓN de los dos números es {numero1 * numero2}")
print(f"La DIVISIÓN de los dos números es {numero1 / numero2}")